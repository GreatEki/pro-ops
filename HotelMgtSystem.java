    private void jbtnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnResetActionPerformed
       
       
        int n = JOptionPane.showConfirmDialog(null, "This will reset the table. All data entered will be wiped! Do You wish to continue?",
                "Action Choice", JOptionPane.YES_NO_CANCEL_OPTION);
        
        if (n == JOptionPane.YES_OPTION) {
                
        DefaultTableModel model = (DefaultTableModel) jtblDetail.getModel();
            model.setRowCount(0);
                txtAddress.setText("");
                txtCustomerRef.setText("");
                txtEmail.setText("");
                txtFirstName.setText("");
                txtMobileNo.setText("");
                txtSubTotal.setText("");
                txtSurname.setText("");
                txtTax.setText("");
                txtTotal.setText("");
                cboGender.setSelectedIndex(0);
                cboIdType.setSelectedIndex(0);
                cboMeal.setSelectedIndex(0);
                cboRoomNo.setSelectedIndex(0);
                cboRoomType.setSelectedIndex(0); 
                cbonNationality.setSelectedIndex(0);
                jDateChooserCheckInDate.setDate(null);
                jDateChooserCheckOutDate.setDate(null);
                jDateChooserDateOfBirth.setDate(null);  
        }
        else if (n == JOptionPane.NO_OPTION || n == JOptionPane.CANCEL_OPTION) {
            JPanel resetPanel = new JPanel();
            resetPanel.setBackground(new Color(101, 200, 160));
            resetPanel.setSize(new Dimension(200, 64));
            resetPanel.setLayout(null);
            
            resetPanel.setVisible(true);
            
            JButton btnOk = new JButton("Click to return");
            resetPanel.add(btnOk);
            btnOk.addActionListener(new ActionListener() {
                public void actionPerformed (ActionEvent e) {
                    
                    resetPanel.setVisible(false);
                }
            });
            
        }
    }//GEN-LAST:event_jbtnResetActionPerformed


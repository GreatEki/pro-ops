    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        DefaultTableModel model = (DefaultTableModel) jtblDetail.getModel();
        if (jtblDetail.getSelectedRow() == -1)
        {
            if (jtblDetail.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Table is empty. No data entered to be deleted", "Quintana Suites Portal", JOptionPane.OK_OPTION);
             }
            else {
                JOptionPane.showMessageDialog(null, "Select a row to delete", "Quintana Suites Portal", JOptionPane.OK_OPTION);
            }
        }
        else {
            model.removeRow(jtblDetail.getSelectedRow());
        }
    }//GEN-LAST:event_jbtnDeleteActionPerformed

